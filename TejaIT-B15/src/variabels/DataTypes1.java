package variabels;

public class DataTypes1 {
public static void main(String[] args) {
	// primitive -8       // Objective
	
	// number data types -4
	byte id=127;          // 1 byte - 8bits
	short num=12123;      // 2 bytes
	int value=701378115;  // 4 bytes
	long mobile=7013781152323232322L;  // 8 bytes
	
	//decimal data types -2
	double amt=12.78; // 8 bytes 
	float miles=1.23f; // 4 bytes  - 6-7 digits after decimal
	
	// char type-1
	char grade='2';  // 1 byte
	// boolean type
	boolean areYouIndian=true; // 1bit
	
	
	System.out.println("Id value::"+id);
	System.out.println("Num ::"+num);
	System.out.println("Int value::"+value);
 }
}
